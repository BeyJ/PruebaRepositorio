    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String seleccion = jComboBox1.getSelectedItem().toString();
        String query = "";

        model.setRowCount(0);
        model.setColumnCount(0);

        try {
            Connection cn = Conexion.conectar();

            if (seleccion.equalsIgnoreCase("Todos")) {
                query = "select * from ingreso";
            } else {
                query = "select * from ingreso where fecha_ingreso = '" + seleccion + "'";
            }
            PreparedStatement pst = cn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            jTable1 = new JTable(model);
            jScrollPane1.setViewportView(jTable1);

            model.addColumn("codigo");
            model.addColumn("fecha");
            model.addColumn("monto");
            model.addColumn("concepto");

            while (rs.next()) {
                Object[] fila = new Object[4];
                for (int i = 0; i < 4; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();

        } catch (SQLException e) {
            System.err.println("Error al recuperar los registros de ingreso." + e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

