    private void pdf_ingresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdf_ingresoActionPerformed

        Document documento = new Document();

        try {
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/Reporte_Ingreso.pdf"));
            documento.open();

            PdfPTable tabla = new PdfPTable(4);
            tabla.addCell("Codigo");
            tabla.addCell("Fecha");
            tabla.addCell("Monto");
            tabla.addCell("Concepto");

            try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/dep_auditoria", "root", "");
                PreparedStatement pst = cn.prepareStatement("select * from ingreso");

                ResultSet rs = pst.executeQuery();

                if (rs.next()) {

                    do {
                        tabla.addCell(rs.getString(1));
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                    } while (rs.next());
                    documento.add(tabla);
                }

            } catch (DocumentException | SQLException e) {

            }
            documento.close();
            JOptionPane.showMessageDialog(null, "Reporte creado");
        } catch (DocumentException | HeadlessException | FileNotFoundException e) {

        }
    }//GEN-LAST:event_pdf_ingresoActionPerformed

